/**
 * Represents the keyboard state with boolean flags for specific keys.
 */
class Keyboard {
      /**
   * @property {boolean} LEFT - Flag indicating if the LEFT arrow key is pressed.
   * @property {boolean} RIGHT - Flag indicating if the RIGHT arrow key is pressed.
   * @property {boolean} UP - Flag indicating if the UP arrow key is pressed.
   * @property {boolean} DOWN - Flag indicating if the DOWN arrow key is pressed.
   * @property {boolean} SPACE - Flag indicating if the SPACE key is pressed.
   * @property {boolean} D - Flag indicating if the D key is pressed.
   */
  
  LEFT = false;
  RIGHT = false;
  UP = false;
  DOWN = false;
  SPACE = false;
  D = false;
}
